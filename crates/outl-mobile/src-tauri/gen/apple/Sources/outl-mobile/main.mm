// `main.mm` is the Tauri-generated entrypoint. We deliberately keep
// **only** two things here:
//
//   1. The pre-`main()` bootstrap that pokes each Swift module's
//      `install()` static method. Obj-C `+load` is the canonical
//      hook that runs before the C `main()` body, and the Swift
//      runtime's equivalents are unreliable on iOS — using a thin
//      `OutlBootstrap` class is the lowest-risk way to fire the
//      Swift code at the right moment in the launch sequence.
//
//   2. The C `main()` itself that calls into Rust via the FFI
//      surface exposed by `bindings.h`. Tauri's iOS template
//      requires this entry point; we can't drop it.
//
// Everything else (toolbar, suggester, swizzle, ops watcher,
// background refresh, brand chrome) lives in `*.swift` next to this
// file and is exposed back to Obj-C via `@objc(ClassName)`. See
// `outl-Swift.h` (generated by Xcode at build time).

#include "bindings/bindings.h"

#import <UIKit/UIKit.h>
#import "outl-Swift.h"

@interface OutlBootstrap : NSObject
@end

@implementation OutlBootstrap

+ (void)load {
    // `+load` runs before `main()`, but UIKit may not be ready yet —
    // hop to the main queue so each Swift `install()` body sees a
    // usable application state.
    dispatch_async(dispatch_get_main_queue(), ^{
        [OutlSwizzle install];
        [OutlBackgroundRefresh install];
        [OutlBrandChrome install];
        [OutlOpsWatcher install];
    });
}

@end

int main(int argc, char *argv[]) {
    ffi::start_app();
    return 0;
}
