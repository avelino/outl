//! Keyboard input handling for the TUI.
//!
//! Key bindings (specified in `crates/outl-tui/CLAUDE.md`):
//!
//! | Key | Action |
//! |-----|--------|
//! | `t` / `home` | journal of today |
//! | `[` | previous journal |
//! | `]` | next journal |
//! | `g j` | journal date picker |
//! | `g p` | page jump (fuzzy) |
//! | `:` | command palette |
//! | `?` | help |
//! | `q` | quit |
